import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))

const readJson = (filePath) => JSON.parse(fs.readFileSync(filePath, 'utf8'))
const escapeSql = (value = '') => value.toString().replaceAll("'", "''")
const q = (value) => `'${escapeSql(value)}'`
const slug = (value = '') =>
  value
    .toString()
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')

const normalizeName = (value = '') =>
  value
    .toString()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, ' ')
    .trim()

const splitName = (fullName = '') => {
  const parts = fullName.toString().trim().split(/\s+/).filter(Boolean)
  if (parts.length <= 1) return { firstName: parts[0] || fullName, lastName: '' }
  return {
    firstName: parts.slice(0, -1).join(' '),
    lastName: parts.slice(-1).join(' '),
  }
}

const teamNameMap = {
  CSK: 'Chennai Super Kings',
  DC: 'Delhi Capitals',
  GT: 'Gujarat Titans',
  KKR: 'Kolkata Knight Riders',
  LSG: 'Lucknow Super Giants',
  MI: 'Mumbai Indians',
  PBKS: 'Punjab Kings',
  RCB: 'Royal Challengers Bengaluru',
  RR: 'Rajasthan Royals',
  SRH: 'Sunrisers Hyderabad',
}

const arg = (name) => {
  const index = process.argv.indexOf(name)
  if (index === -1) return ''
  return process.argv[index + 1] || ''
}

const outFile = arg('--out')
const matchesFile = arg('--matches')

const iplPlayersJson = readJson(path.resolve(__dirname, '../mocks/iplLatestPlayers.json'))
const squadState = readJson(path.resolve(__dirname, '../mocks/state/team-squads.json'))
const contestState = readJson(path.resolve(__dirname, '../mocks/state/contests.json'))
const userState = readJson(path.resolve(__dirname, '../mocks/state/store-users.json'))
const rawMatchInput = matchesFile
  ? readJson(path.resolve(process.cwd(), matchesFile))
  : readJson(path.resolve(__dirname, '../mocks/state/tournament-matches.json'))['ipl-2026'] || []

const toUtcIso = (startAt = '', timezone = 'UTC') => {
  if (!startAt) return ''
  if (startAt.endsWith('Z') || /[+-]\d\d:\d\d$/.test(startAt)) {
    return new Date(startAt).toISOString()
  }
  if (timezone === 'Asia/Kolkata') {
    return new Date(`${startAt}:00+05:30`).toISOString()
  }
  return new Date(`${startAt}:00Z`).toISOString()
}

const matchState = Array.isArray(rawMatchInput)
  ? rawMatchInput
  : Array.isArray(rawMatchInput?.matches)
    ? rawMatchInput.matches.map((match) => ({
        id: match.id || `ipl-m${match.matchNo}`,
        matchNo: match.matchNo,
        name:
          match.name ||
          `Match ${match.matchNo}: ${match.home || match.teamA} vs ${match.away || match.teamB}`,
        teamA: match.home || match.teamAKey || match.teamA,
        teamB: match.away || match.teamBKey || match.teamB,
        teamAKey: match.teamAKey || match.home || match.teamA,
        teamBKey: match.teamBKey || match.away || match.teamB,
        startTime: toUtcIso(match.startAt || match.startTime, match.timezone || 'UTC'),
        status: match.status || 'notstarted',
        venue: match.venue || match.location || '',
      }))
    : []

const jsonPlayers = Array.isArray(iplPlayersJson)
  ? iplPlayersJson
  : iplPlayersJson.players || iplPlayersJson.data || iplPlayersJson.list || []

const jsonByName = new Map(jsonPlayers.map((row) => [normalizeName(row.name), row]))
const iplSquads = squadState.filter((row) => row.tournament === 'IPL 2026')
const auctionContest = contestState.find((row) => row.id === 'nwmsu-ipl-auction')
if (!auctionContest) {
  throw new Error('nwmsu-ipl-auction not found in mock contests')
}

const flattenedPlayers = []
for (const squad of iplSquads) {
  for (const player of squad.squad || []) {
    const fullName = (player.name || '').toString().trim()
    if (!fullName) continue
    const jsonRow = jsonByName.get(normalizeName(fullName)) || null
    const { firstName, lastName } = splitName(fullName)
    flattenedPlayers.push({
      sourceKey: jsonRow?.id || `manual:${squad.teamCode}:${slug(fullName)}`,
      displayName: fullName,
      firstName,
      lastName,
      role: (jsonRow?.role || player.role || 'BAT').toString().toUpperCase(),
      teamKey: squad.teamCode,
      teamName: squad.teamName || teamNameMap[squad.teamCode] || squad.teamCode,
      country: (jsonRow?.nationality || player.country || 'India').toString(),
      imageUrl: (jsonRow?.player_img || player.imageUrl || '').toString(),
      active: player.active !== false,
      basePrice: Number(jsonRow?.base_price || 0) || 0,
    })
  }
}

const players = [...new Map(flattenedPlayers.map((row) => [row.sourceKey, row])).values()].sort(
  (a, b) => (a.teamKey + a.displayName).localeCompare(b.teamKey + b.displayName),
)

const sql = []
sql.push('-- Generated by api/scripts/generate-ipl-auction-seed.js')
sql.push(`-- Matches included: ${matchState.length}. Supply --matches <file> for the full IPL 2026 schedule.`)
sql.push('begin;')
sql.push('')
sql.push('-- 1) tournament')
sql.push(`insert into tournaments (name, season, status, source_key, created_at, updated_at)
values ('IPL 2026', '2026', 'active', 'ipl-2026', now(), now())
on conflict (source_key) do update
set name = excluded.name,
    season = excluded.season,
    status = excluded.status,
    updated_at = now();`)
sql.push('')
sql.push('-- 2) team squad metadata')
for (const squad of iplSquads.sort((a, b) => a.teamCode.localeCompare(b.teamCode))) {
  sql.push(`insert into team_squads (
  team_code, team_name, tournament_type, country, league, tournament, source, created_at, updated_at
) values (
  ${q(squad.teamCode)},
  ${q(squad.teamName || teamNameMap[squad.teamCode] || squad.teamCode)},
  'league',
  'india',
  'IPL',
  'IPL 2026',
  'json-import',
  now(),
  now()
) on conflict (team_code) do update
set team_name = excluded.team_name,
    tournament_type = excluded.tournament_type,
    country = excluded.country,
    league = excluded.league,
    tournament = excluded.tournament,
    source = excluded.source,
    updated_at = now();`)
}
sql.push('')
sql.push('-- 3) players')
for (const player of players) {
  sql.push(`insert into players (
  first_name, last_name, role, team_key, team_name, player_id, display_name, country,
  image_url, active, batting_style, bowling_style, base_price, source_key, created_at, updated_at
) values (
  ${q(player.firstName)},
  ${q(player.lastName)},
  ${q(player.role)},
  ${q(player.teamKey)},
  ${q(player.teamName)},
  ${q(slug(player.displayName))},
  ${q(player.displayName)},
  ${q(player.country)},
  ${q(player.imageUrl)},
  ${player.active ? 'true' : 'false'},
  '',
  '',
  ${Number.isFinite(player.basePrice) ? player.basePrice : 0},
  ${q(player.sourceKey)},
  now(),
  now()
) on conflict (source_key) do update
set first_name = excluded.first_name,
    last_name = excluded.last_name,
    role = excluded.role,
    team_key = excluded.team_key,
    team_name = excluded.team_name,
    player_id = excluded.player_id,
    display_name = excluded.display_name,
    country = excluded.country,
    image_url = excluded.image_url,
    active = excluded.active,
    base_price = excluded.base_price,
    updated_at = now();`)
}
sql.push('')
sql.push('-- 4) matches')
for (const match of matchState) {
  const sourceKey = (match.id || slug(match.name || `${match.teamA}-vs-${match.teamB}`)).toString()
  sql.push(`with tournament_row as (
  select id from tournaments where source_key = 'ipl-2026'
)
insert into matches (
  tournament_id, name, team_a, team_b, team_a_key, team_b_key, start_time, source_key, status, created_at, updated_at
)
select
  tournament_row.id,
  ${q(match.name)},
  ${q(match.teamA)},
  ${q(match.teamB)},
  ${q(match.teamAKey || match.teamA)},
  ${q(match.teamBKey || match.teamB)},
  ${q(match.startTime || match.startAt)},
  ${q(sourceKey)},
  ${q(match.status || 'scheduled')},
  now(),
  now()
from tournament_row
on conflict (source_key) do update
set name = excluded.name,
    team_a = excluded.team_a,
    team_b = excluded.team_b,
    team_a_key = excluded.team_a_key,
    team_b_key = excluded.team_b_key,
    start_time = excluded.start_time,
    status = excluded.status,
    updated_at = now();`)
}
sql.push('')
sql.push('-- 5) contest shell')
const matchSourceKeys = (auctionContest.matchIds || []).map((id) => q(id)).join(', ')
sql.push(`with tournament_row as (
  select id from tournaments where source_key = 'ipl-2026'
), selected_matches as (
  select array_agg(id order by start_time asc) as ids
  from matches
  where source_key = any(array[${matchSourceKeys}]::text[])
)
insert into contests (
  tournament_id, name, match_ids, prize_structure, game, mode, source_key, status,
  entry_fee, max_participants, participants_count, created_at, updated_at
)
select
  tournament_row.id,
  'NWMSU-IPL-AUCTION',
  coalesce(selected_matches.ids, '{}'::bigint[]),
  '{}'::jsonb,
  'Fantasy',
  'fixed_roster',
  'nwmsu-ipl-auction',
  'Starting Soon',
  0,
  100,
  0,
  now(),
  now()
from tournament_row
cross join selected_matches
on conflict (source_key) do update
set name = excluded.name,
    match_ids = excluded.match_ids,
    game = excluded.game,
    mode = excluded.mode,
    status = excluded.status,
    updated_at = now();`)
sql.push('')
sql.push('-- 6) participant joins and fixed rosters')
for (const participant of auctionContest.fixedParticipants || []) {
  const user = userState.find((row) => row.userId === participant.userId)
  const rosterSourceKeys = (participant.roster || [])
    .map((name) => {
      const jsonRow = jsonByName.get(normalizeName(name))
      if (jsonRow?.id) return jsonRow.id
      const found = players.find((row) => normalizeName(row.displayName) === normalizeName(name))
      return found?.sourceKey || `missing:${slug(name)}`
    })
    .filter(Boolean)
    .map(q)
    .join(', ')

  sql.push(`-- participant: ${participant.name}`)
  sql.push(`with contest_row as (
  select id from contests where source_key = 'nwmsu-ipl-auction'
), user_row as (
  select id from users where lower(user_id) = lower(${q(participant.userId)})
), player_rows as (
  select array_agg(id order by id asc) as ids
  from players
  where source_key = any(array[${rosterSourceKeys}]::text[])
)
insert into contest_joins (contest_id, user_id, joined_at)
select contest_row.id, user_row.id, now()
from contest_row
join user_row on true
on conflict (contest_id, user_id) do nothing;`)

  sql.push(`with contest_row as (
  select id from contests where source_key = 'nwmsu-ipl-auction'
), user_row as (
  select id from users where lower(user_id) = lower(${q(participant.userId)})
), player_rows as (
  select array_agg(id order by id asc) as ids
  from players
  where source_key = any(array[${rosterSourceKeys}]::text[])
)
insert into contest_fixed_rosters (contest_id, user_id, player_ids, created_at, updated_at)
select contest_row.id, user_row.id, coalesce(player_rows.ids, '{}'::bigint[]), now(), now()
from contest_row
join user_row on true
cross join player_rows
on conflict (contest_id, user_id) do update
set player_ids = excluded.player_ids,
    updated_at = now();`)

  if (!user) {
    sql.push(`-- WARNING: ${participant.userId} was not found in mock store-users. Ensure this user exists in prod before running the roster insert.`)
  }
}
sql.push('')
sql.push(`update contests c
set participants_count = (
  select count(*) from contest_joins cj where cj.contest_id = c.id
)
where c.source_key = 'nwmsu-ipl-auction';`)
sql.push('')
sql.push('commit;')

const output = `${sql.join('\n')}\n`
if (outFile) {
  fs.writeFileSync(path.resolve(process.cwd(), outFile), output, 'utf8')
  console.log(`Wrote ${outFile}`)
} else {
  process.stdout.write(output)
}
